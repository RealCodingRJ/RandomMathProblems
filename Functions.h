#pragma once

inline auto Add(const int a, const int b) -> int {
    return a + b;
}


inline auto Sub(const int a, const int b) -> int {
    return a - b;
}

inline auto Mul(const int a, const int b) -> int {
    return a * b;
}

inline auto Div(const int a, const int b) -> int {
    return a / b;
}